# Use the dot(.) to invoke a method
p "Hello World!".length # 12
puts "Samuel Amkam".length # 12
puts "Samuel Amkam".upcase # prints in uppercase
puts "HELLO WORLD".downcase # prints in lowercase

student_name = 'Web Avenger'
puts student_name.upcase
puts student_name.downcase
puts student_name.capitalize # prints first letter in capitals

p 10
p 10.next # eleven
p -1.next

puts "Hello World!"
puts "Hello World!".inspect